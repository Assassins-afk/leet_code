class Solution:
    def lengthOfLastWord(self, s: str) -> int:
        # Инициализируем два указателя:
        # i - начинаем с последнего символа строки
        # length - счетчик длины последнего слова
        i, length = len(s) - 1, 0
       
        # Первый цикл: пропускаем все пробелы в конце строки
        # Двигаемся справа налево, пока встречаем пробелы
        while s[i] == " ":
            i -= 1
            
        # Второй цикл: считаем символы последнего слова
        # Условия: не вышли за границы строки (i >= 0) 
        # И текущий символ не пробел (s[i] != " ")
        while i >= 0 and s[i] != " ":
            length += 1  # Увеличиваем счетчик длины слова
            i -= 1       # Двигаемся к предыдущему символу
            
        # Возвращаем подсчитанную длину последнего слова
        return length