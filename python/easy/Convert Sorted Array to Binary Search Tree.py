class Solution:
    def sortedArrayToBST(self, nums: List[int]) -> Optional[TreeNode]:
        """
        Преобразует отсортированный массив в сбалансированное бинарное дерево поиска (BST).
        
        Args:
            nums: Отсортированный по возрастанию массив целых чисел
            
        Returns:
            Корень сбалансированного BST
        """

        # Вспомогательная рекурсивная функция для построения дерева
        # l - левая граница текущего подмассива 
        # r - правая граница текущего подмассива 
        def helper(l, r):
            # если левая граница больше правой,
            # значит подмассив пуст - возвращаем None
            if l > r:
                return None

            # Находим средний элемент текущего подмассива
            # Используем целочисленное деление для получения индекса
            m = (l + r) // 2
            
            # Создаем корневой узел из среднего элемента
            root = TreeNode(nums[m])
            
            # Рекурсивно строим левое поддерево:
            # берем элементы от l до m-1 (все, что меньше текущего)
            root.left = helper(l, m - 1)
            
            # Рекурсивно строим правое поддерево:
            # берем элементы от m+1 до r (все, что больше текущего)
            root.right = helper(m + 1, r)
            
            return root

        # Начальная левая граница = 0, правая = индекс последнего элемента
        return helper(0, len(nums) - 1)