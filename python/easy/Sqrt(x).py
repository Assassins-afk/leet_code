class Solution:
    def mySqrt(self, x: int) -> int:
        # Обработка базовых случаев: корень из 0 = 0, корень из 1 = 1
        if x == 0 or x == 1:
            return x

        # Инициализируем границы бинарного поиска
        start = 1  # Левая граница (минимально возможный корень)
        end = x    # Правая граница (максимально возможный корень)

        # Бинарный поиск: продолжаем пока левая граница не пересечёт правую
        while start <= end:
            # Находим среднее значение (потенциальный корень)
            mid = (start + end) // 2

            # Вычисляем квадрат среднего значения
            mid_sq = mid * mid
        
            # Если квадрат точно равен x, мы нашли точный корень
            if mid_sq == x:
                return mid
            
            # Если квадрат меньше x, то искомый корень больше текущего mid
            if mid_sq < x:
                start = mid + 1     # Сдвигаем левую границу вправо
                ans = mid           # Сохраняем mid как возможный ответ (на случай, если точного корня нет)
            
            # Если квадрат больше x, то искомый корень меньше текущего mid
            else:
                end = mid - 1       # Сдвигаем правую границу влево
        
        # Если точный корень не найден (число не является полным квадратом),
        # возвращаем последнее сохранённое значение ans — целую часть корня
        # Это наибольшее число, квадрат которого <= x
        return ans