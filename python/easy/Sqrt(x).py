import math  # Импортируем модуль math для использования встроенных математических функций

class Solution:
    def mySqrt(self, x: int) -> int:
        # Используем функцию math.isqrt() для вычисления целочисленного квадратного корня
        # isqrt() возвращает наибольшее целое число, квадрат которого меньше или равен x
        result = math.isqrt(x)
        
        return result  # Возвращаем результат - целую часть квадратного корня

