class Solution:
    def addBinary(self, a: str, b: str) -> str:
        res = ''  # Строка для хранения результата
        carry = 0  # Переменная для хранения переноса (как при сложении в столбик)

        # Переворачиваем строки, чтобы складывать справа налево (от младших разрядов к старшим)
        a, b = a[::-1], b[::-1]
        
        # Проходим по всем разрядам, длина цикла = максимальной длине из двух строк
        for i in range(max(len(a), len(b))):
            # Получаем цифру из строки a, если индекс существует, иначе 0
            # ord(a[i]) - ord("0") преобразует символ '0' или '1' в число 0 или 1
            digitA = ord(a[i]) - ord("0") if i < len(a) else 0
            
            # Аналогично получаем цифру из строки b
            digitB = ord(b[i]) - ord("0") if i < len(b) else 0

            # Суммируем обе цифры и перенос с предыдущего разряда
            total = digitA + digitB + carry
            
            # Получаем текущий бит результата (остаток от деления на 2)
            char = str(total % 2)
            
            # Добавляем бит в начало результата (так как мы идём от младших разрядов)
            res = char + res
            
            # Вычисляем перенос в следующий разряд (целая часть от деления на 2)
            carry = total // 2

        # Если после обработки всех разрядов остался перенос, добавляем его в начало
        if carry:
            res = '1' + res
        
        return res  # Возвращаем итоговую строку с двоичным числом